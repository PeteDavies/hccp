                        <h2 class="bigText">Early Years Foundation Stage Framework</h2> 
                       
                        <p>	


In Every Child Matters the government set out aims to help all children and young people achieve five outcomes that children themselves have identified as being most important to them: being healthy, staying safe, enjoying and achieving, making a positive contribution and achieving economic well being.
</p>

<p>
From September 2008 all schools and Ofsted registered early years settings attended by children from birth to the end of the academic year in which they turn five have to meet the requirements of the Early Years Foundation Stage (EYFS) which reflects these outcomes.  Providers have a duty to ensure that their Early Years provision meets both the learning and development requirements and the welfare requirements.
</p>
<p>
The EYFS has four guiding themes which work together to underpin effective practice in its delivery.  The four themes of the EYFS are:</p>
                        
                        <h3>1.  A Unique Child</h3>
                        <p>Every child is a competent learner from birth who can be resilient, capable, competent and self assured</p>

                        <h3>2.  Positive Relationships</h3>
                        <p>Children learn to be strong and independent from a base of loving and secure relationships with parents and/or a key person.</p>

                        <h3>3.  Enabling Environments</h3>
                        <p>The environment plays a key role in supporting and extending children's development and learning.</p>

                        <h3>4.  Learning and Development</h3>
                        <p>Children develop and learn in different ways and at different rates and all areas of learning and development are equally important and inter-connected.</p>
                    
                    
                         <h3 class="bigText">Observation, assessment and planning</h3> 
                         <p>All of our planning starts with observing children, in order to understand and consider their interests, development and learning. Our key worker system means that children have their learning journey documented. These documents consist of photographs, written observations and samples of children's work and will be presented to you when your child leaves pre-school. We encourage parents and carers to share these learning journeys with their child's reception teacher.</p>

                          <h3 class="bigText">The Wider context</h3> 
                         <p>We work in partnership with other settings, other professionals and with individuals and groups in the community to support children's development and progress towards the outcomes of Every Child Matters: being healthy, staying safe, enjoying and achieving, making a positive contribution and economic well-being.</p>
